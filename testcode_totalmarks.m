    c1q1=c1q1*0
elseif (c1q2<c1q1) && (c1q2<c1q3) && (c1q2<c1q4)
    c1q2=c1q2*0
elseif (c1q3<c1q2) && (c1q3<c1q1) && (c1q3<c1q4)
    c1q3=c1q3*0
elseif (c1q4<c1q2) && (c1q4<c1q3) && (c1q4<c1q1)
    c1q4=c1q4*0
elseif (c1q4==c1q2) && (c1q4==c1q3) && (c1q4==c1q1)
    c1q4=c1q4*0
else c1q1=c1q1+0
end
