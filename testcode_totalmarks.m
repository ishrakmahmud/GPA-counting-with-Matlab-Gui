    c1at3=24
elseif (85<=c1at) && (c1at<=89)
    c1at3=18
elseif (80<=c1at) && (c1at<=84)
    c1at3=12
elseif (75<=c1at) && (c1at<=79)
    c1at3=6
elseif (0<=c1at) && (c1at<=74)
    c1at3=0
